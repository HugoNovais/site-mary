function mostrar() {
    document.getElementById("mostrar").style.display = 'block';
}

function menu() {

    let modal = document.querySelector('.modal')

    modal.style.display = 'block';
    document.getElementsById("emmo").style.display = 'block';
}

function fechar() {
    let modal = document.querySelector('.modal')

    modal.style.display = 'none';
}

